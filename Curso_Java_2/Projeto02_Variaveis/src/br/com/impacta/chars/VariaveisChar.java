package br.com.impacta.chars;

public class VariaveisChar {
	public static void main(String[] args) {
		
		char ch1 = 'X';
		char ch2 = 65;
		char ch3 = '\n';
		char ch4 = 27554;
		
		System.out.println("ch1: " + ch1);
		System.out.println("ch3: " + ch3);
		System.out.println("ch2: " + ch2);
		System.out.println("ch4: " + ch4);
		
		
	}

}
